
public class PrefixOp {

	public static void main(String[] args) {
	int num1 =7;
	int num2 =7;
	System.out.println(++num1);
	System.out.println(++num1);
	System.out.println(num1);
	
	System.out.println("______");
	
	System.out.println(num2++);
	System.out.println(num2++);
	System.out.println(num2);
	}

}
